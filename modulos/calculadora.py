def suma(n1, n2):
    """ retorna la suma de dos enteros """
    return n1 + n2

def resta(n1, n2):
    """ retorna la resta de dos enteros """
    return n1 - n2

def multiplicacion(n1, n2):
    """ retorna la multimplicación de dos enteros """
    return n1 * n2

def division(n1, n2):
    """ retorna la división de dos enteros """
    return n1 / n2